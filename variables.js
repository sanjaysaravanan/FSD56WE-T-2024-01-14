// Declaration
// giving a name and declaring the type of the varaible ( var/let/const )
var a;

// var allows re-declaration & re-assignment
var a;

// Initialization/assignment
a = 10;

// Re-assignment or value change
a = true;
a = ["sam", true];

// let
let x;

// let doesn't allow re-declaration
// let x;
x = 99;

// let allows re-assignment
x = 100;
x = ["sam", true];
x = false;

// const
// does not allow re-declaration & re-assignment
// has to be initalized in the same line of declaration
const y = 0;
